<template>
  <div class="mod-demo-echarts">
    <el-row :gutter="20">
    <el-col :span="24">
        <el-card>
          <div id="J_chartLineBox" class="chart-box"></div>
        </el-card>
      </el-col>
    </el-row>
  </div>
</template>

<script>
  export default {
    data() {
      return {
        chartLine: null,
        chartBar: null,
        chartPie: null,
        chartScatter: null
      }
    },
    mounted () {
      this.initChartLine()
    },
    activated () {
      // 由于给echart添加了resize事件, 在组件激活时需要重新resize绘画一次, 否则出现空白bug
      if (this.chartLine) {
        this.chartLine.resize()
      }
      if (this.chartBar) {
        this.chartBar.resize()
      }
      if (this.chartPie) {
        this.chartPie.resize()
      }
      if (this.chartScatter) {
        this.chartScatter.resize()
      }
    },
    methods: {
      initChartLine () {
        var option = {
          'title': {
            'text': '访问量统计'
          },
          'tooltip': {
            'trigger': 'axis'
          },
          'legend': {
            'data': [ '访问量']
          },
          'grid': {
            'left': '3%',
            'right': '4%',
            'bottom': '3%',
            'containLabel': true
          },
          'toolbox': {
            'feature': {
              'saveAsImage': { }
            }
          },
          'xAxis': {
            'type': 'category',
            'boundaryGap': false,
            'data': [ '五天前', '四天前', '三天前', '二天前', '一天前', '今天' ]
          },
          'yAxis': {
            'type': 'value'
          },
          'series': [
            {
              'name': '访问量',
              'type': 'line',
              'stack': '总量',
              'data': [ 120, 132, 101, 134, 90, 230]
            }
          ]
        }
        this.chartLine = echarts.init(document.getElementById('J_chartLineBox'))
        this.chartLine.setOption(option)
        window.addEventListener('resize', () => {
          this.chartLine.resize()
        })
      },
    },
  }
</script>

